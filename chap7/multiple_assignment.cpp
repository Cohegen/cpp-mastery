/*
C++ allows a convenient method of assigning many variable the same value:
using multiple  assigments
*/

#include <iostream>
using namespace std;

int main()
{
    int count,incr,index;

    count=incr=index = 10;

    cout <<"Count:" << count;
    cout << "Incr: " << incr;
    cout << "Index: " << index;


    return 0;
}
