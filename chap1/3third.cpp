//using an operator

#include <iostream>
using namespace std;

int main()
{
    int length; //declaration of the variable length
    int width; // declaration of the width
    int area; //declaration of area

    length = 7; // assigns 7 to length
    width = 5; // assigns 5 to width

    area = length * width; //compute area

    cout << "The area is";
    cout << area; // this displays 35

    return 0;
}
